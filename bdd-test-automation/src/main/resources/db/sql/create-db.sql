CREATE TABLE CUSTOMER(
CUSTOMER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 35434, INCREMENT BY 1) PRIMARY KEY, 
PASSWORD VARCHAR(10), 
FIRSTNAME VARCHAR(50),
LASTNAME VARCHAR(50),
PHONE VARCHAR(15),
EMAIL VARCHAR(30),
NATIONAL_ID VARCHAR(15),
ADDRESS VARCHAR(200),
POSTALCODE VARCHAR(12),
CITY VARCHAR(50),
COUNTRY VARCHAR(50)
);

CREATE TABLE ACCOUNT(
ACCOUNT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 12212234, INCREMENT BY 1) PRIMARY KEY, 
CUSTOMER_ID INTEGER, 
ACCOUNT_TYPE VARCHAR(50),
BRANCH_ID VARCHAR(50),
BRANCH_NAME VARCHAR(100),
LAST_TRANSACTION_DATE TIMESTAMP,
AMOUNT DOUBLE,
ADDRESS VARCHAR(200),
POSTALCODE INTEGER,
CITY VARCHAR(50),
COUNTRY VARCHAR(50)
);

